// Scaffold a stochastic-image QA gate for a new agent.
//
// Generates a self-contained JavaScript file with:
//   - qaCheckSticker_<agent> function (Gemini Flash + vision, retry-on-429,
//     deterministic severity bucketing, Levenshtein softening)
//   - <agent>VisualQAStep Mastra step (id: <agent>-visual-qa, never throws)
//   - normalizeForCompare + levenshtein helpers (dependency-free)
//   - A header comment listing the workflow + publish wiring still needed
//
// Usage:
//   node scaffold-qa-gate.mjs --agent <name> --quote-step <step-id> [--output <path>]
//
// After running:
//   1. Customize the rubric items in the generated qaCheckSticker function
//      to match the agent's brand spec (colors, brandmark name, etc.)
//   2. Wire into the Mastra workflow (see references/threading.md)
//   3. Thread qaResult through agent-runner.js and publish.js
//      (see references/threading.md for the exact patches)
//
// No dependencies — runs on plain Node.js >=18.

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

function parseArgs(argv) {
  const args = { agent: null, quoteStep: null, output: null, help: false };
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--help' || a === '-h') args.help = true;
    else if (a === '--agent' && argv[i + 1]) { args.agent = argv[++i]; }
    else if (a === '--quote-step' && argv[i + 1]) { args.quoteStep = argv[++i]; }
    else if (a === '--output' && argv[i + 1]) { args.output = argv[++i]; }
    else { console.error(`Unknown arg: ${a}`); args.help = true; }
  }
  return args;
}

